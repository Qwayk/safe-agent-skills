def _operation_from_entry(entry: InventoryEntry) -> Operation:
    if not entry.cli_command:
        raise ValueError(f"Inventory entry '{entry.name}' has no CLI command")
    sample_args = entry.sample_args or _default_sample_args(entry)
    return Operation(
        name=entry.name,
        section=entry.section,
        description=entry.description,
        method=entry.method,
        path=entry.path,
        cli_command=entry.cli_command,
        safety=entry.safety,
        doc_url=entry.doc_url,
        sample_args=sample_args,
    )


OPERATIONS: tuple[Operation, ...] = tuple(
    _operation_from_entry(entry) for entry in INVENTORY if entry.status.lower().startswith("impl")
)
