@dataclass(frozen=True)
class Operation:
    name: str
    section: str
    description: str
    method: str
    path: str
    cli_command: str
    safety: Sequence[str]
    doc_url: str
    sample_args: Sequence[str] = ()


@dataclass(frozen=True)
class InventoryEntry:
    name: str
    section: str
    description: str
    method: str
    path: str
    status: str
    doc_url: str
    safety: Sequence[str]
    cli_command: str | None = None
    sample_args: Sequence[str] = ()


_PLACEHOLDER_SAMPLE_VALUES: dict[str, str] = {
    "voice_id": "voice-123",
    "generated_voice_id": "voice-preview-1",
    "dubbing_id": "dub-foo",
    "speaker_id": "speaker-1",
    "resource_id": "resource-1",
    "service_account_user_id": "svc-acc",
    "documentation_id": "doc-1",
    "project_id": "proj-1",
    "token_type": "tts_websocket",
}


def _default_sample_args(entry: InventoryEntry) -> tuple[str, ...]:
    if not entry.cli_command:
        return ()
    args: list[str] = []
    placeholders = re.findall(r"{([^}]+)}", entry.path)
    for placeholder in placeholders:
        args.append(f"--{placeholder.replace('_', '-')}")
        args.append(_PLACEHOLDER_SAMPLE_VALUES.get(placeholder, "value"))
    method = entry.method.upper()
    if method not in {"GET", "DELETE", "WEBSOCKET"}:
        args.extend(["--body", '{"example":"value"}'])
    safety = tuple(entry.safety or ())
    needs_out = "binary_output" in safety or ("sensitive_output" in safety and "write" in safety)
    if needs_out:
        suffix = "bin" if "binary_output" in safety else "json"
        args.extend(["--out", f"sample.{suffix}"])
    return tuple(args)


